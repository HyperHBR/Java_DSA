public class _08_Switch {
    public static void main(String[] args) {
    char X = 'd';

    switch(X){
    case 'a':   System.out.println("Apple");
                break;
    case 'X':   System.out.println("Banana");
                break;
    case 'd':   System.out.println("Carrot");
                break;
    default:    System.out.println("Watermelon");
    }
}
}
