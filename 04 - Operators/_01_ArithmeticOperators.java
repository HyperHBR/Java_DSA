public class _01_ArithmeticOperators{
    public static void main(String args[]){
        int A = 10;
        int B = 5;

        //Addition
        System.out.println("Addition Of 10 And 5 Is  = "  + (A+B));
        //SUbtraction
        System.out.println("Subtraction Of 10 And 5 Is  = "  + (A-B));
        //Multiplication
        System.out.println("Multiplication Of 10 And 5 Is  = "  + (A*B));
        //Division
        System.out.println("Division Of 10 And 5 Is  = "  + (A/B));
        //Modulo
        System.out.println("Modulo Of 10 And 5 Is  = "  + (A%B));
    }
}

