public class _05_AssignmentOperators {
    public static void main(String[] args) {
        //Simple Assignment
        int A = 10;
        System.out.println("Initial Value Of A : " + A);
        
        // OtherAssignment Operators
        A = 10;
        System.out.println("10 += 5 : " + (A+=5));
        // OtherAssignment Operators
        A = 10;
        System.out.println("10 -= 5 : " + (A-=5));
        // OtherAssignment Operators
        A = 10;
        System.out.println("10 *= 5 : " + (A*=5));
        // OtherAssignment Operators
        A = 10;
        System.out.println("10 /= 5 : " + (A/=5));
    }
}
