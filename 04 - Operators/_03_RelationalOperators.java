public class _03_RelationalOperators {
    public static void main(String[] args) {
        int A = 5;
        int B = 10;
        System.out.println("Value Of A = " + A);
        System.out.println("Value Of B = " + B);

        System.out.println("\nA == B : " + (A==B));
        System.out.println("A != B : " + (A!=B));
        System.out.println("A < B : " + (A<B));
        System.out.println("A <= B : " + (A<=B));
        System.out.println("A > B : " + (A>B));
        System.out.println("A >= B : " + (A>=B));
    }
}
